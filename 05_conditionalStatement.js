//if-else statement: 
let salary = 20000000;

if(salary > 10000000){
    console.log("You are rich...😊.");
}else{
    console.log("You are living your normal life...🥱.");
}

//if-else-if-else Statement
let grade = "B";

if(grade == "A"){
    console.log("You are genious...❤️.");
}else if(grade == "B"){
    console.log("Your are amazing...😍.");
}else if(grade == "C"){
    console.log("You are average...😊.");
}else{
    console.log("You need to work hard...🥲.");
}

//Switch Statement:
let per = 90;

switch(per){
    case 90:
        console.log("Excellent...");
        break;
    case 80:
        console.log("Amazing...");
        break;
    case 70:
        console.log("Good...");
        break;
    case 60:
        console.log("Poor...");
        break;
}